    public void crearPDF(String asientos, List<String> lstAsientos){
        javax.swing.JTextField nombreT = new javax.swing.JTextField();
        javax.swing.JTextField tarjetaT = new javax.swing.JTextField();
        javax.swing.JTextField fechaT = new javax.swing.JTextField();
        javax.swing.JTextField cvvT = new javax.swing.JTextField();
            
        Object[] campos = {"Titular:", nombreT, "Tarjeta:", tarjetaT, "MM/AA:", fechaT, "CVV:", cvvT};
        boolean datosValidos = false;

        while (!datosValidos) {
            int op = javax.swing.JOptionPane.showConfirmDialog(this, campos, "Pago", javax.swing.JOptionPane.OK_CANCEL_OPTION);

            // Si el usuario presiona "Cancelar" o cierra la ventana con la 'X', salimos por completo
            if (op != javax.swing.JOptionPane.OK_OPTION) {
                for (String asiento : lstAsientos) {
                    liberarAsiento(asiento); // Cancelamos reserva
                } 
                
                return;     
            }

            try {
                String titular = nombreT.getText().trim();
                String tarjeta = tarjetaT.getText().trim();
                String fecha = fechaT.getText().trim();
                String cvv = cvvT.getText().trim();

                // validar nombre
                for (int i = 0; i < titular.length(); i++) {
                    char c = titular.charAt(i);
                    if (c >= '0' && c <= '9') {
                        throw new Exception("El nombre no puede contener números.");
                    }
                }

                // validar tarjeta
                if (tarjeta.length() != 6) {
                    throw new Exception("La tarjeta debe tener 6 números.");
                }
                for (int i = 0; i < tarjeta.length(); i++) {
                    char c = tarjeta.charAt(i);
                    if (c < '0' || c > '9') {
                        throw new Exception("La tarjeta solo debe contener números.");
                    }
                }

                // validar cvv
                if (cvv.length() != 3) {
                    throw new Exception("El CVV debe tener 3 números.");
                }
                for (int i = 0; i < cvv.length(); i++) {
                    char c = cvv.charAt(i);
                    if (c < '0' || c > '9') {
                        throw new Exception("El CVV solo debe contener números.");
                    }
                }

                // validar fecha
                if (fecha.length() != 5 || fecha.charAt(2) != '/') {
                    throw new Exception("La fecha debe tener formato MM/AA.");
                }

                try {
                    int mes = Integer.parseInt(fecha.substring(0, 2));
                    int anio = Integer.parseInt(fecha.substring(3, 5)) + 2000;

                    if (mes < 1 || mes > 12) {
                        throw new Exception("El mes (MM) debe ser un número válido entre 01 y 12.");
                    }

                    java.time.YearMonth actual = java.time.YearMonth.now();
                    int anioActual = actual.getYear();
                    int mesActual = actual.getMonthValue();

                    if (anio < anioActual || (anio == anioActual && mes < mesActual)) {
                        throw new Exception("La tarjeta ya está vencida. Debe ser posterior o igual a la fecha actual.");
                    }

                } catch (NumberFormatException e) {
                    throw new Exception("Los valores de mes y año deben ser puramente numéricos.");
                }

                datosValidos = true; 
                
                
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Error de Validación", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
        
        String destino="", fecha_salida="", hora_salida="", fecha_llegada="", hora_llegada="";
        String puerta_salida="", puerta_embarque="", precio="", num_vuelo="";
        String nomb = user;
            
        try (Connection con = DBConn.conectar()) {
                
            // Datos del Vuelo
            PreparedStatement ps = con.prepareStatement("SELECT * FROM vuelos WHERE num_vuelo = ?");
            ps.setString(1, idV);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                destino = rs.getString("destino");
                fecha_salida = rs.getString("fecha_salida");
                hora_salida = rs.getString("hora_salida");
                fecha_llegada = rs.getString("fecha_llegada");
                hora_llegada = rs.getString("hora_llegada");
                puerta_salida = rs.getString("puerta_salida");
                puerta_embarque = rs.getString("puerta_embarque");
                precio = rs.getString("precio");
                num_vuelo = rs.getString("num_vuelo");
            }

            // Datos del Usuario
            PreparedStatement psU = con.prepareStatement("SELECT nombre, apellidoP, apellidoM FROM usuarios WHERE user = ?");
            psU.setString(1, user);
            ResultSet rsU = psU.executeQuery();
            
                if (rsU.next()) {
                    nomb = rsU.getString("nombre") + " " + rsU.getString("apellidoP") + " " + rsU.getString("apellidoM");
                }
                
        } catch (Exception e) {
            e.printStackTrace();
        }

        //GENERAR PDF
        Document document = new Document();
            
        try {
            String dest = "Ticket_Vuelo_" + idV + ".pdf";
            PdfWriter.getInstance(document, new FileOutputStream(dest));
            document.open();
            
            Font fontTitulo = new Font(Font.FontFamily.HELVETICA, 16, Font.BOLD, com.itextpdf.text.BaseColor.WHITE);
            Font fontLabel = new Font(Font.FontFamily.HELVETICA, 9, Font.NORMAL, com.itextpdf.text.BaseColor.GRAY);
            Font fontDato = new Font(Font.FontFamily.HELVETICA, 11, Font.BOLD, com.itextpdf.text.BaseColor.BLACK);
            com.itextpdf.text.BaseColor colorAzul = new com.itextpdf.text.BaseColor(29, 152, 183);

            //encabezado
            PdfPTable enc = new PdfPTable(2);
            enc.setWidthPercentage(100);
            enc.setWidths(new float[]{3, 1});

            //Celda de Texto encabezado lado izq
            PdfPCell cellT = new PdfPCell(new Paragraph("BOLETO DE ABORDAJE", fontTitulo));
            cellT.setBackgroundColor(colorAzul);
            cellT.setBorder(PdfPCell.NO_BORDER);
            cellT.setPadding(15);
            enc.addCell(cellT);

                //Celda encabezado que contendrá la Celda del Logo
                try {
                    java.net.URL logoUrl = getClass().getResource("/img/iconb.png");
                    Image logo = Image.getInstance(logoUrl);
                    logo.scaleToFit(30, 30);

                    //celda logo
                    PdfPTable celda_logo = new PdfPTable(1);
                    
                    //Celda específica del logo
                    PdfPCell logoin = new PdfPCell(logo);
                    logoin.setBorder(PdfPCell.NO_BORDER); // Quitar borde
                    logoin.setHorizontalAlignment(Element.ALIGN_RIGHT);
                    logoin.setPadding(10);
                    
                    // Metemos la celda logo en su tabla
                    celda_logo.addCell(logoin);
                    
                    // Finalmente, metemos esa tabla dentro de la celda header
                    PdfPCell headerLogo = new PdfPCell(celda_logo);
                    headerLogo.setBackgroundColor(colorAzul);
                    headerLogo.setBorder(PdfPCell.NO_BORDER);
                    headerLogo.setVerticalAlignment(Element.ALIGN_MIDDLE);
                    
                    enc.addCell(headerLogo);
                } catch (Exception e) {
                    // Celda de respaldo en caso de error
                    PdfPCell empty = new PdfPCell(new Paragraph(num_vuelo, fontTitulo));
                    empty.setBackgroundColor(colorAzul);
                    empty.setBorder(PdfPCell.NO_BORDER);
                    enc.addCell(empty); }

            document.add(enc);

            //fila 1
            PdfPTable tabla1 = new PdfPTable(3);
            tabla1.setWidthPercentage(100);
            tabla1.addCell(crearCeldaDato("PASAJERO", nomb.toUpperCase(), fontLabel, fontDato));
            tabla1.addCell(crearCeldaDato("VUELO", idV, fontLabel, fontDato));
            tabla1.addCell(crearCeldaDato("DESTINO", destino, fontLabel, fontDato));
            document.add(tabla1);

            //fila 2
            PdfPTable tabla2 = new PdfPTable(2);
            tabla2.setWidthPercentage(100);
            tabla2.addCell(crearCeldaDato("SALIDA (" + fecha_salida + ")", hora_salida, fontLabel, fontDato));
            tabla2.addCell(crearCeldaDato("LLEGADA (" + fecha_llegada + ")", hora_llegada, fontLabel, fontDato));
            document.add(tabla2);
            
            //fila 3
            PdfPTable tabla3 = new PdfPTable(4);
            tabla3.setWidthPercentage(100);
            tabla3.addCell(crearCeldaDato("P. SALIDA", puerta_salida, fontLabel, fontDato));
            tabla3.addCell(crearCeldaDato("EMBARQUE", puerta_embarque, fontLabel, fontDato));
            tabla3.addCell(crearCeldaDato("ASIENTO(S)", asientos, fontLabel, fontDato));
            tabla3.addCell(crearCeldaDato("PRECIO", "$" + precio, fontLabel, fontDato));
            document.add(tabla3);
            
            document.add(new Paragraph("\nPresente este ticket digital al abordar.", fontLabel));
            document.close();
            
            Desktop.getDesktop().open(new File(dest));
        
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
