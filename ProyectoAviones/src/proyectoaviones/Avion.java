    private void pagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarActionPerformed
        String[] filas = {"A", "B", "C", "D", "E", "F", "G"};
        int seleccionados = 0;
        List<String> lstAsientos = new ArrayList<>();

        for (int i = 0; i < asientos.length; i++) {
            if (asientos[i].isSelected() && asientos[i].isEnabled()) {

                // Calculamos el nombre del asiento (A1, A2... G6) según el índice i
                String letraFila = filas[i / 6];
                int numColumna = (i % 6) + 1;
                String nombreAsiento = letraFila + numColumna;
                
                registrarAsiento(nombreAsiento);
                seleccionados++;
                lstAsientos.add(nombreAsiento);
            }
        }

        if (seleccionados > 0) {
            String asientos = String.join(", ", lstAsientos);
            crearPDF(asientos,lstAsientos);
            
            new Vuelos(user).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione al menos un asiento para continuar.");
        }
    }//GEN-LAST:event_pagarActionPerformed

